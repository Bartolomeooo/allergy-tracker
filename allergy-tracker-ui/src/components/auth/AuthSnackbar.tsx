import {Snackbar, Alert} from '@mui/material';

interface AuthSnackbarProps {
  open: boolean;
  message: string;
  severity: 'success' | 'error';
  onClose: () => void;
}

export default function AuthSnackbar({
  open,
  message,
  severity,
  onClose,
}: AuthSnackbarProps) {
  return (
    <Snackbar
      open={open}
      autoHideDuration={5000}
      onClose={onClose}
      anchorOrigin={{vertical: 'bottom', horizontal: 'center'}}
    >
      <Alert
        onClose={onClose}
        severity={severity}
        variant="filled"
        sx={{width: '100%'}}
      >
        {message}
      </Alert>
    </Snackbar>
  );
}
